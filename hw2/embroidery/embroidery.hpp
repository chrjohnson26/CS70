#include <cs70/turtle.hpp>
using namespace std;

/**
 * \brief          Draws a rectangle     
 * \param t        Turtle object
 * \param width    Rectangle width
 * \param height   Rectangle height
 * \warning        Height/width must be positive   
 */
void rect(Turtle& t, float width, float height);

/**
 * \brief          Draws a rectangle and writes a message     
 * \warning        Height/width must be positive   
 */
void meetTurtle();

/**
 * \brief          Draws a rectangular border, the 1992, and graphs the normalized data     
 * \warning        Height/width must be positive   
 */
void plotExampleData();

/**
 * \brief          Draws a rectangular border, the text, and graphs the normalized data     
 * \warning        Height/width must be positive   
 */
void plotStudentData();
